#include "3-calc.h"
/**
 * op_add - adds 2 numbers
 * @a: integer 1
 * @b: integer 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract 2 numbers
 * @a: integer 1
 * @b: integer 2
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies 2 numbers
 * @a: integer 1
 * @b: integer 2
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides 2 numbers
 * @a: integer 1
 * @b: integer 2
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - reminder of division
 * @a: integer 1
 * @b: integer 2
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
